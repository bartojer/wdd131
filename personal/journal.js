function journalHeaderTemplate(month) {
    let monthName;
    switch (month) {
        case 1:
            monthName = 'January';
            break;
        case 2:
            monthName = 'February';
            break;
        case 3:
            monthName = 'March';
            break;
        case 4:
            monthName = 'April';
            break;
        case 5:
            monthName = 'May';
            break;
        case 6:
            monthName = 'June';
            break;
        case 7:
            monthName = 'July';
            break;
        case 8:
            monthName = 'August';
            break;
        case 9:
            monthName = 'September';
            break;
        case 10:
            monthName = 'October';
            break;
        case 11:
            monthName = 'November';
            break;
        case 12:
            monthName = 'December';
            break;
        default:
            monthName = month;
    }
    return `<h2>${monthName}</h2>`;
}

function journalCardTemplate(journalEntry) {
    return `<div class="entry-card">
        ${journalEntry.imgSrc ? `<img src="${journalEntry.imgSrc}" alt="${journalEntry.imgAlt}">` : ''}
        <p>${journalEntry.text.replace(/\n/g, '<br>')}</p>
    </div>`;
}


function journalPageTemplate(month, year) {
    let html = journalHeaderTemplate(month);
    let monthEntries = journalEntries.filter(entry => entry.month === month && entry.year === year);
    html += monthEntries.map(journalCardTemplate).join('');

    const journalContent = document.querySelector(".entries");
    if (journalContent) {
        journalContent.innerHTML = html;
    }
}

document.querySelector("#november_button").addEventListener("click", () => {
    journalPageTemplate(11, 2024);
})
document.querySelector("#december_button").addEventListener("click", () => {
    journalPageTemplate(12, 2024);
})
document.querySelector("#january_button").addEventListener("click", () => {
    journalPageTemplate(1, 2025);
})
document.querySelector("#february_button").addEventListener("click", () => {
    journalPageTemplate(2, 2025);
})

function init() {
    const currentDate = new Date();
    const currentMonth = currentDate.getMonth() + 1;
    const currentYear = currentDate.getFullYear();
    journalPageTemplate(currentMonth, currentYear);
}

init()